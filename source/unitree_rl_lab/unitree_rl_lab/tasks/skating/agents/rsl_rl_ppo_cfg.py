"""RSL-RL PPO runner configurations for roller-skating.

Two configs are defined:
  - SkatingPhase1PPORunnerCfg : gliding balance, no AMP
  - SkatingEurekaPPORunnerCfg : Eureka-driven full skating (Phase 2+)

AMP is disabled for the Eureka phase because the task-specific reward is
already generated by the LLM; style guidance is left to the reward function
itself (the LLM can use push_off_rhythm / skate_foot_edge_contact helpers).
Enabling AMP is straightforward — set amp["enabled"] = True and point to the
reference motion NPZ if desired.
"""

import os

from isaaclab.utils import configclass
from isaaclab_rl.rsl_rl import RslRlOnPolicyRunnerCfg, RslRlPpoActorCriticCfg, RslRlPpoAlgorithmCfg

_REFERENCE_MOTION_NPZ = os.path.normpath(os.path.join(
    os.path.dirname(__file__),
    "..", "..", "..", "data", "skating_reference.npz",
))

# Human reference motion generated by scripts/skating/retarget_video_to_amp.py
# (YouTube skating video → GENMO SMPL → G1 retargeting → 85-dim AMP obs)
_HUMAN_REFERENCE_NPZ = os.path.normpath(os.path.join(
    os.path.dirname(__file__),
    "..", "..", "..", "data", "skating_reference_human.npz",
))


# ─────────────────────────────────────────────────────────────────────────────
# Phase 1: Gliding balance — pure PPO, no AMP
# ─────────────────────────────────────────────────────────────────────────────

@configclass
class SkatingPhase1PPORunnerCfg(RslRlOnPolicyRunnerCfg):
    """Phase 1: Gliding balance — pure PPO, no AMP (robot just needs to stay upright)."""

    num_steps_per_env = 24
    max_iterations = 2000
    save_interval = 200
    experiment_name = "skating_phase1"
    empirical_normalization = False

    policy = RslRlPpoActorCriticCfg(
        init_noise_std=1.0,
        actor_hidden_dims=[512, 256, 128],
        critic_hidden_dims=[512, 256, 128],
        activation="elu",
    )
    algorithm = RslRlPpoAlgorithmCfg(
        value_loss_coef=1.0,
        use_clipped_value_loss=True,
        clip_param=0.2,
        entropy_coef=0.01,
        num_learning_epochs=5,
        num_mini_batches=4,
        learning_rate=1.0e-3,
        schedule="adaptive",
        gamma=0.99,
        lam=0.95,
        desired_kl=0.01,
        max_grad_norm=1.0,
    )

    amp: dict = {"enabled": False}


